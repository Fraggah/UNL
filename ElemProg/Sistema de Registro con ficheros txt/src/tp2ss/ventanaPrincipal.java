    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String documento=JOptionPane.showInputDialog("Ingrese el DNI del Alumno a borrar");
        if(listaAlumnos.eliminarAlumno(documento)){
            JOptionPane.showMessageDialog(null,"Eliminado correctamente");
        }
        else{
            JOptionPane.showMessageDialog(null,"Acción denegada, revise que el DNI sea el correcto");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

