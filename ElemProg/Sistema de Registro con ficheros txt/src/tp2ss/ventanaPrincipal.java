    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nom, doc, mat, cal;
        int not;
        nom = txtNombre.getText();
        doc = txtDni.getText();
        mat = txtMateria.getText();
        not = Integer.parseInt(txtNota.getText());
        switch (not){
        case 1:
        case 2:
        case 3:
        case 4:
        case 5:
        cal = "Insuficiente";
        break;
        case 6:
        cal= "Aprobado";
        break;
        case 7:
        cal= "Bueno";
        break;
        case 8:
        cal= "Muy Bueno";
        break;
        case 9:
        cal = "Distinguido";
        break;
        case 10:
        cal= "Sobresaliente";
        break;
        default:
        cal = "Error "+not+" no corresponde a una nota";
    }   
        
        
        Alumno alumno = new Alumno(nom,doc,mat,cal);
       
        listaAlumnos.insertarAlumno(alumno);
        JOptionPane.showMessageDialog(null,"Alumno agregado Correctamente");
        txtNombre.setText("");
        txtDni.setText("");
        txtMateria.setText("");
        txtNota.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

