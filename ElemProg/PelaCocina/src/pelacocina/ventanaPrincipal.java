    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        int tvacio, tcostillas, tchorizos, tmorcilla, tmatambre, tverduras, tzapallo;
        int tiempofinal;
        int horas, minutos;
        String recomendacion="";
        tvacio = Integer.parseInt(txtVacio.getText());
        tcostillas = Integer.parseInt(txtCostillas.getText());
        tchorizos = Integer.parseInt(txtChorizos.getText());
        tmorcilla = Integer.parseInt(txtMorcilla.getText());
        tmatambre = Integer.parseInt(txtMatambre.getText());
        tverduras = Integer.parseInt(txtVerduras.getText());
        tzapallo = Integer.parseInt(txtZapallo.getText());
        
        if(chkRelleno.isSelected()){
        tvacio = tvacio*3;
        }
        if(chkBarbacoa.isSelected()){
            tcostillas = tcostillas*3;
        }
        if(chkPizza.isSelected()){
            tmatambre = tmatambre*3;
        }
        tiempofinal =90+tvacio*10+tcostillas*10+tchorizos*5+tmorcilla*7+tmatambre*12+tverduras*12+tzapallo*10;
        horas = tiempofinal/60;
        minutos = tiempofinal%60;
   
        etiResultado.setText(horas+"hs "+minutos+" min");
        
        if (horas>7){
            recomendacion="PELA LCDTH!";
        }
        else{
        switch(horas){
            case 1:recomendacion="Hoy el Pela vino a full! \n Asi que preparate un vinito y anda poniendo la mesa";
            break;
            case 2:recomendacion="Tiempo normal, ideal para una picadita:\n chizitos, papitas, palitos, etc";
            break;
            case 3:recomendacion="Tiempo standard del Pela, intenta tomar algo\n para mantenerte relajado mientras disfrutas de la picadita";
            break;
            case 4:recomendacion="Tranquilo, pensa en que la comida va a estar \ndeliciosa, pero es recomendable que otro\n se encargue de hacer un plato de entrada";
            break;
            case 5:recomendacion="Preparar un plato de entrada para comer\na la hora, una vez pasadas las 3 horas tener un primer plato \ny dejar como comida central a las 5 horas la comida del Pela";
            break;
            case 6:recomendacion="Podes ir pensando en picar algo y tomarte\nuna siesta, pensando que cuando despiertes tendras un platazo!";
            break;
            case 7:recomendacion="Es inevitable sacar al Pela de la pariila";
            break;
        }
        }
        
        txaSalida.setText("Recomendacion: "+recomendacion);
    }//GEN-LAST:event_btnCalcularActionPerformed

